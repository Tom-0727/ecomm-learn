function getQuestionsByModule(moduleId) {
  if (moduleId === 0) return EXAM_QUESTIONS.filter(q => q.moduleId === 0);
  return EXAM_QUESTIONS.filter(q => q.moduleId === moduleId);
}

function getQuestionsByDifficulty(level) {
  return EXAM_QUESTIONS.filter(q => q.difficulty === level);
}

function getRandomQuestions(n, moduleIds = null, excludeIds = []) {
  let pool = EXAM_QUESTIONS.filter(q => !excludeIds.includes(q.id));
  if (moduleIds) pool = pool.filter(q => moduleIds.includes(q.moduleId));
  const shuffled = pool.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, n);
}
